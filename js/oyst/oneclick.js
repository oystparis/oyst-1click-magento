"use strict";

function showMessage(txt, type) {
    var html = "<ul class=\"messages\"><li class=\"" + type + "\"-msg\"><ul><li>" + txt + "</li></ul></li></ul>";
    $("messages").update(html);
    var url = window.location.href.split("#")[0];
    window.location.replace(url + "#html-body");
}

